	public static String parseRegionCode(String regionValue){
		return parseRegionCode( regionValue, "US");
	}
	public static String parseRegionCode(String regionValue, String countryCode){
		if(null == countryCode || "".equals(countryCode.trim())){
			countryCode = "US";
		}
		if(regionValue != null && !"".equals(regionValue.trim())){
			regionValue = regionValue.trim();
			regionValue = regionValue.replaceAll("\\.", "");
			regionValue = regionValue.toUpperCase();
			//If just two characters assume code
			if(regionValue.length() == 2){
				return regionValue.toUpperCase();
			}else{
				if("US".equalsIgnoreCase(countryCode) || "USA".equalsIgnoreCase(countryCode) || "United States".equalsIgnoreCase(countryCode) ||
						"CA".equalsIgnoreCase(countryCode) || "CANADA".equalsIgnoreCase(countryCode)){
					String inferedCode = UsCanadaRegionMap.REGION_NAME_TO_CODE_MAP.get(regionValue);
					if( null != inferedCode){
						return inferedCode;
					}
				}
			}
		}
		return regionValue;
	}
